package iterator;

public class Student {
	private String name;
	private String hakbun;
	private double java;
	public Student(String name, String hakbun, int java) {
		super();
		this.name = name;
		this.hakbun = hakbun;
		this.java = java;
	}
	
	public String getName() {
		return name;
	}
	
	public String getHakBun() {
		return hakbun;
	}
	public double getJava() {
		return java;
	}
	

}
